package engine.solvers;

/**
 * Enumeration representing different types of terrain on a golf course.
 */
public enum TerrainType {
    /**
     * Represents grassy terrain.
     */
    Grass,

    /**
     * Represents water terrain.
     */
    Water,

    /**
     * Represents sandy terrain.
     */
    Sand,

    /**
     * Represents tree terrain.
     */
    Tree
}
